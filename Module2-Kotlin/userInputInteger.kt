package Kotlin_Assignments

//Kotlin Program to Print an Integer (Entered by the User)

fun main() {
    print("Enter An Integer 1 : ")
    var num = readLine()!!.toInt()
    println(num)

    print("Enter An Integer 2 : ")
    var num1 = readLine()!!.toFloat()
    println(num1)

    print("Enter An Integer 3 : ")
    var num2 = readLine()!!.toDouble()
    println(num2)
}